import pandas as pd

def getPrices(data):
    cur = data.splitlines()
    out = {}
    arrClosed = []
    arrOpen = []

    for x in cur:
        y = x.split("\t")
        close = y[1].replace(",", "")
        open = y[2].replace(",", "")
        arrClosed.append(float(close))
        arrOpen.append(float(open))

    arrClosed.reverse()
    arrOpen.reverse()
    out["price"] = arrClosed
    out["open"] = arrOpen

    # load data into a DataFrame object:
    df = pd.DataFrame(out)

    return df

